def verify_invite_code(code: str) -> bool:
    """Verify if invite code is active, matches DB records, and has available uses."""
    code = code.strip()
    if not code:
        return False
        
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    cur.execute("SELECT max_uses, used_count, status FROM invite_codes WHERE code = ?", (code,))
    row = cur.fetchone()
    conn.close()
    
    if row:
        max_uses, used_count, status = row[0], row[1], row[2]
        return status == "active" and used_count < max_uses
    return False

def generate_referral_code(username: str, max_uses: int = 1, allotted_credits: int = None) -> str:
    """Generate a unique referral invite code for a user."""
    username_clean = username.strip().upper()
    random_suffix = secrets.token_hex(3).upper()
    code = f"REF_{username_clean}_{random_suffix}"
    
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    cur.execute(
        "INSERT INTO invite_codes (code, created_by, max_uses, used_count, allotted_credits) VALUES (?, ?, ?, 0, ?)",
        (code, username, max_uses, allotted_credits)
    )
    conn.commit()
    conn.close()
    return code

def get_user_referral_codes(username: str) -> pd.DataFrame:
    """Retrieve all codes generated by a user."""
    conn = sqlite3.connect(DB_FILE)
    df = pd.read_sql_query(
        "SELECT code, max_uses, used_count, status, allotted_credits, created_at FROM invite_codes WHERE created_by = ? ORDER BY created_at DESC",
        conn,
        params=(username,)
    )
    conn.close()
    return df

def get_referral_audit_log() -> pd.DataFrame:
    """Retrieve audit log mapping registered users to their referrers and codes."""
    conn = sqlite3.connect(DB_FILE)
    df = pd.read_sql_query(
        """
        SELECT 
            u.username AS registered_user,
            u.created_at AS registered_at,
            u.referred_by_code AS referral_code,
            ic.created_by AS referred_by_employee,
            ic.allotted_credits AS initial_credits,
            u.credits_remaining AS remaining_credits
        FROM users u
        LEFT JOIN invite_codes ic ON u.referred_by_code = ic.code
        ORDER BY u.created_at DESC
        """,
        conn
    )
    conn.close()
    return df

def register_user(username: str, password: str, email: str = None, 
                  sq1: str = None, sa1: str = None,
                  sq2: str = None, sa2: str = None,
                  referred_by_code: str = None) -> bool:
    """Insert a new user record and link them to their referrer invite code."""
    username = username.strip()
    if not username:
        raise ValueError("Username cannot be empty.")
    if len(password) < 4:
        raise ValueError("Password must be at least 4 characters long.")
    if not sq1 or not sa1 or not sq2 or not sa2:
        raise ValueError("Both security questions and answers must be filled out.")
    if sq1 == sq2:
        raise ValueError("Please select two different security questions.")
    
    referred_by_code = referred_by_code.strip() if referred_by_code else None
    if referred_by_code:
        if not verify_invite_code(referred_by_code):
            raise ValueError("Invalid or expired invite referral code.")
            
    pwd_hash, salt = hash_password(password)
    
    # Secure salt for questions
    salt_questions = secrets.token_hex(16)
    sa1_clean = sa1.strip().lower()
    sa2_clean = sa2.strip().lower()
    
    sa1_hash, _ = hash_password(sa1_clean, salt_questions)
    sa2_hash, _ = hash_password(sa2_clean, salt_questions)
    
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    try:
        # Get allotted credits from invite code if any
        allotted_credits = None
        if referred_by_code:
            cur.execute("SELECT allotted_credits FROM invite_codes WHERE code = ?", (referred_by_code,))
            row = cur.fetchone()
            if row:
                allotted_credits = row[0]
                
        # Insert user
        encrypted_email = crypto_utils.encrypt_data(email) if email else None
        cur.execute(
            """
            INSERT INTO users (
                username, password_hash, salt, email,
                security_question_1, security_answer_hash_1,
                security_question_2, security_answer_hash_2,
                salt_questions, referred_by_code, credits_remaining
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (username, pwd_hash, salt, encrypted_email, sq1, sa1_hash, sq2, sa2_hash, salt_questions, referred_by_code, allotted_credits)
        )
        # Update code count
        if referred_by_code:
            cur.execute(
                "UPDATE invite_codes SET used_count = used_count + 1 WHERE code = ?",
                (referred_by_code,)
            )
        conn.commit()
        return True
    except sqlite3.IntegrityError:
        raise ValueError(f"Username '{username}' already exists. Please choose a different one.")
    finally:
        conn.close()


