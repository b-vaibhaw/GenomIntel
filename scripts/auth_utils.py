def check_authentication():
    """Gatekeeper function. If not authenticated, displays login/registration/recovery forms and stops execution."""
    if "authenticated" not in st.session_state:
        st.session_state["authenticated"] = False
        st.session_state["username"] = None

    # Hide default Streamlit sidebar navigation globally (we use custom st.page_link widgets)
    st.markdown(
        """
        <style>
            [data-testid="stSidebarNav"] {
                display: none !important;
            }
        </style>
        """,
        unsafe_allow_html=True
    )

    if not st.session_state["authenticated"]:
        # Custom CSS when not authenticated
        st.markdown(
            """
            <style>
                .main {
                    background-color: #0f1116;
                    color: #e2e8f0;
                }
            </style>
            """,
            unsafe_allow_html=True
        )
        
        # Center login box beautifully
        col_space1, col_form, col_space2 = st.columns([1, 2, 1])
        with col_form:
            st.title("🔒 BioIntelligence Platform")
            st.subheader("Secure Access Gateway")
            st.write("Please log in, register a free account, or recover your password to continue.")
            
            auth_tab1, auth_tab2, auth_tab3 = st.tabs(["🔑 Sign In", "📝 Create Account", "❓ Forgot Password"])
            
            with auth_tab1:
                with st.form("login_form"):
                    username = st.text_input("Username")
                    password = st.text_input("Password", type="password")
                    submit = st.form_submit_button("Sign In", use_container_width=True)
                    if submit:
                        if authenticate_user(username, password):
                            st.session_state["authenticated"] = True
                            st.session_state["username"] = username
                            st.success("Successfully logged in!")
                            st.rerun()
                        else:
                            st.error("Invalid username or password.")
                            
            with auth_tab2:
                with st.form("register_form"):
                    st.write("**Account Details**")
                    new_user = st.text_input("Username")
                    new_email = st.text_input("Email (optional)")
                    new_pass = st.text_input("Password (min 4 chars)", type="password")
                    confirm_pass = st.text_input("Confirm Password", type="password")
                    
                    st.write("---")
                    st.write("**Security Questions (for Password Recovery)**")
                    sq1 = st.selectbox("Security Question 1", SECURITY_QUESTIONS, index=0)
                    sa1 = st.text_input("Answer 1")
                    sq2 = st.selectbox("Security Question 2", SECURITY_QUESTIONS, index=1)
                    sa2 = st.text_input("Answer 2")
                    
                    st.write("---")
                    st.write("**Referral Verification**")
                    invite_code = st.text_input("Invite / Referral Code", placeholder="Invite code (Default: BIOINTEL2026)")
                    
                    submit = st.form_submit_button("Create Account", use_container_width=True)
                    if submit:
                        if not invite_code.strip():
                            st.error("An invite or referral code is required to register.")
                        elif not verify_invite_code(invite_code):
                            st.error("Invalid, expired, or fully-used referral code.")
                        elif not new_user.strip() or not new_pass.strip():
                            st.error("Username and password cannot be empty.")
                        elif new_pass != confirm_pass:
                            st.error("Passwords do not match.")
                        elif not sa1.strip() or not sa2.strip():
                            st.error("Please answer both security questions.")
                        elif sq1 == sq2:
                            st.error("Please select two different security questions.")
                        else:
                            try:
                                register_user(
                                    username=new_user, 
                                    password=new_pass, 
                                    email=new_email if new_email.strip() else None,
                                    sq1=sq1, 
                                    sa1=sa1, 
                                    sq2=sq2, 
                                    sa2=sa2,
                                    referred_by_code=invite_code
                                )
                                st.success("Account created successfully! Please sign in using the Sign In tab.")
                            except ValueError as e:
                                st.error(str(e))
                                
            with auth_tab3:
                st.write("**Reset Password via Security Questions**")
                
                if "recovery_username" not in st.session_state:
                    st.session_state["recovery_username"] = ""
                if "recovery_questions" not in st.session_state:
                    st.session_state["recovery_questions"] = None
                
                rec_user = st.text_input("Enter your Username", value=st.session_state["recovery_username"])
                if st.button("Retrieve Security Questions", use_container_width=True):
                    if rec_user.strip():
                        questions = get_user_security_questions(rec_user)
                        if questions:
                            st.session_state["recovery_questions"] = questions
                            st.session_state["recovery_username"] = rec_user.strip()
                        else:
                            st.error("Username not found.")
                            st.session_state["recovery_questions"] = None
                    else:
                        st.error("Please enter a username.")
                
                if st.session_state["recovery_questions"]:
                    st.write("---")
                    st.info("Username verified. Please answer your security questions to set a new password:")
                    
                    with st.form("recovery_answers_form"):
                        q1, q2 = st.session_state["recovery_questions"][0], st.session_state["recovery_questions"][1]
                        st.write(f"**Q1:** {q1}")
                        ans1 = st.text_input("Answer 1", key="ans1_rec")
                        
                        st.write(f"**Q2:** {q2}")
                        ans2 = st.text_input("Answer 2", key="ans2_rec")
                        
                        st.write("---")
                        new_pwd = st.text_input("New Password", type="password")
                        confirm_new_pwd = st.text_input("Confirm New Password", type="password")
                        
                        submit_reset = st.form_submit_button("Reset Password", use_container_width=True)
                        if submit_reset:
                            if not ans1.strip() or not ans2.strip():
                                st.error("Please answer both questions.")
                            elif new_pwd != confirm_new_pwd:
                                st.error("Passwords do not match.")
                            elif len(new_pwd) < 4:
                                st.error("Password must be at least 4 characters.")
                            else:
                                success = reset_password_via_questions(
                                    username=st.session_state["recovery_username"],
                                    sa1=ans1,
                                    sa2=ans2,
                                    new_password=new_pwd
                                )
                                if success:
                                    st.success("Password reset successfully! You can now log in using the Sign In tab.")
                                    st.session_state["recovery_username"] = ""
                                    st.session_state["recovery_questions"] = None
                                else:
                                    st.error("Incorrect security answers. Password reset failed.")
        st.stop()
    else:
        # Show username and logout button in sidebar
        st.sidebar.markdown("---")
        st.sidebar.markdown(f"👤 Logged in as: **{st.session_state['username']}**")
        if st.sidebar.button("Log Out", use_container_width=True):
            st.session_state["authenticated"] = False
            st.session_state["username"] = None
            st.rerun()

def render_referral_dashboard():
    """Renders the referral auditing and generation forms in Streamlit UI."""
    st.header("👤 Team Referrals & Access Logs")
    st.write("Generate unique invite codes for new employees and track who shared their codes and who signed up.")
    
    col_gen, col_codes = st.columns([1, 2])
    username = st.session_state.get("username")
    
    with col_gen:
        st.subheader("Generate Invite Code")
        max_uses = st.number_input("Max Allowed Uses", min_value=1, max_value=1000, value=1)
        is_limited = st.checkbox("Credit Limited (Demo)", value=False)
        allotted_credits = None
        if is_limited:
            allotted_credits = st.number_input("Allotted Credits", min_value=1, max_value=100, value=3)
            
        if st.button("Generate Invite Code", use_container_width=True):
            code = generate_referral_code(username, max_uses, allotted_credits)
            st.success(f"Generated Code: `{code}`")
            st.rerun()
            
    with col_codes:
        st.subheader("Your Generated Invite Codes")
        df_my_codes = get_user_referral_codes(username)
        if not df_my_codes.empty:
            # Map None/NaN to 'Unlimited' for display
            df_my_codes["allotted_credits"] = df_my_codes["allotted_credits"].apply(
                lambda x: "Unlimited" if pd.isna(x) or x is None else int(x)
            )
            df_my_codes.columns = ["Invite Code", "Max Uses", "Used Count", "Status", "Allotted Credits", "Created At"]
            st.dataframe(df_my_codes, use_container_width=True)
        else:
            st.info("You haven't generated any invite codes yet.")
            
    st.markdown("---")
    st.subheader("Audit Log: Registered Employees & Referrers")
    st.write("Track exactly who registered, when they signed up, and which employee's invite code was used.")
    
    df_audit = get_referral_audit_log()
    if not df_audit.empty:
        df_audit["initial_credits"] = df_audit["initial_credits"].apply(
            lambda x: "Unlimited" if pd.isna(x) or x is None else int(x)
        )
        df_audit["remaining_credits"] = df_audit["remaining_credits"].apply(
            lambda x: "Unlimited" if pd.isna(x) or x is None else int(x)
        )
        df_audit.columns = [
            "Registered User", "Registered Date", "Invite Code Used", 
            "Inviting Employee / Owner", "Initial Credits", "Remaining Credits"
        ]
        st.dataframe(df_audit, use_container_width=True)
    else:
        st.info("No referral registration logs found.")
