def authenticate_user(username: str, password: str) -> bool:
    """Authenticate credentials against database."""
    username = username.strip()
    if not username:
        return False
        
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    cur.execute("SELECT password_hash, salt FROM users WHERE username = ?", (username,))
    row = cur.fetchone()
    conn.close()
    
    if row:
        pwd_hash, salt = row[0], row[1]
        return verify_password(password, pwd_hash, salt)
    return False

def get_user_security_questions(username: str) -> list:
    """Retrieve security questions registered by the user."""
    username = username.strip()
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    cur.execute("SELECT security_question_1, security_question_2 FROM users WHERE username = ?", (username,))
    row = cur.fetchone()
    conn.close()
    return list(row) if row else None

def reset_password_via_questions(username: str, sa1: str, sa2: str, new_password: str) -> bool:
    """Validate answers and update user password in SQLite database."""
    username = username.strip()
    if len(new_password) < 4:
        raise ValueError("Password must be at least 4 characters long.")
        
    conn = sqlite3.connect(DB_FILE)
    cur = conn.cursor()
    
    cur.execute(
        "SELECT security_answer_hash_1, security_answer_hash_2, salt_questions FROM users WHERE username = ?",
        (username,)
    )
    row = cur.fetchone()
    
    if not row:
        conn.close()
        return False
        
    sa1_hash, sa2_hash, salt_questions = row[0], row[1], row[2]
    
    sa1_clean = sa1.strip().lower()
    sa2_clean = sa2.strip().lower()
    
    verify_sa1 = verify_password(sa1_clean, sa1_hash, salt_questions)
    verify_sa2 = verify_password(sa2_clean, sa2_hash, salt_questions)
    
    if verify_sa1 and verify_sa2:
        new_pwd_hash, new_salt = hash_password(new_password)
        cur.execute(
            "UPDATE users SET password_hash = ?, salt = ? WHERE username = ?",
            (new_pwd_hash, new_salt, username)
        )
        conn.commit()
        conn.close()
        return True
    else:
        conn.close()
        return False

