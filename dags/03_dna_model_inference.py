def _check_variants_available(**context: Any) -> None:
    """Verify that the variants table has rows before running inference."""
    conn = dag_utils.get_pg_conn()
    try:
        result = dag_utils.fetch_one(conn, "SELECT COUNT(*) AS n FROM variants")
        n = result["n"] if result else 0
        log.info("Variants in DB: %d", n)
        if n == 0:
            raise ValueError(
                "variants table is empty — run genetics_qc_pca_pipeline first."
            )
    finally:
        conn.close()

    run_id = dag_utils.log_pipeline_start(DAG_ID, config={"step": "dna_model_inference"})
    dag_utils.push_run_id(context, run_id)
    log.info("Started pipeline run_id=%s", run_id)


