with DAG(
    dag_id=DAG_ID,
    description="DNABERT-2 + HyenaDNA inference, LLM narrative ensemble, Superset refresh",
    default_args=default_args,
    schedule=None,
    start_date=datetime(2024, 1, 1),
    catchup=False,
    max_active_runs=1,
    tags=["biointel", "dna-models", "llm", "inference", "superset"],
) as dag:

    # ------------------------------------------------------------------
    # Task 1: Check variants available
    # ------------------------------------------------------------------
    check_variants_available = PythonOperator(
        task_id="check_variants_available",
        python_callable=_check_variants_available,
    )

    # ------------------------------------------------------------------
    # Task 2: Download DNABERT-2 weights
    # ------------------------------------------------------------------
    download_dnabert2_weights = PythonOperator(
        task_id="download_dnabert2_weights",
        python_callable=_download_dnabert2_weights,
        execution_timeout=timedelta(hours=4),
    )

    # ------------------------------------------------------------------
    # Task 3: Download HyenaDNA weights
    # ------------------------------------------------------------------
    download_hyenadna_weights = PythonOperator(
        task_id="download_hyenadna_weights",
        python_callable=_download_hyenadna_weights,
        execution_timeout=timedelta(hours=4),
    )

    # ------------------------------------------------------------------
    # Task 4: DNABERT-2 inference
    # ------------------------------------------------------------------
    run_dnabert2_inference = PythonOperator(
        task_id="run_dnabert2_inference",
        python_callable=_run_dnabert2_inference,
        execution_timeout=timedelta(hours=6),
    )

    # ------------------------------------------------------------------
    # Task 5: HyenaDNA inference
    # ------------------------------------------------------------------
    run_hyenadna_inference = PythonOperator(
        task_id="run_hyenadna_inference",
        python_callable=_run_hyenadna_inference,
        execution_timeout=timedelta(hours=6),
    )

    # ------------------------------------------------------------------
    # Task 6: LLM ensemble narrative generation
    # ------------------------------------------------------------------
    run_llm_ensemble_narrative = PythonOperator(
        task_id="run_llm_ensemble_narrative",
        python_callable=_run_llm_ensemble_narrative,
        execution_timeout=timedelta(hours=8),
    )

    # ------------------------------------------------------------------
    # Task 7: Refresh Superset dashboards
    # ------------------------------------------------------------------
    update_superset_dashboards = PythonOperator(
        task_id="update_superset_dashboards",
        python_callable=_update_superset_dashboards,
        execution_timeout=timedelta(minutes=15),
    )

    # ------------------------------------------------------------------
    # Task 8: Mark pipeline run complete
    # ------------------------------------------------------------------
    update_pipeline_run = PythonOperator(
        task_id="update_pipeline_run",
        python_callable=_update_pipeline_run,
        trigger_rule="all_done",
    )

    # ------------------------------------------------------------------
    # Task dependencies
    #
    #   check_variants_available
    #       ├── download_dnabert2_weights ──► run_dnabert2_inference ──►┐
    #       └── download_hyenadna_weights ──► run_hyenadna_inference ──►┤
    #                                                                    ▼
    #                                              run_llm_ensemble_narrative
    #                                                         │
    #                                              update_superset_dashboards
    #                                                         │
    #                                               update_pipeline_run
    # ------------------------------------------------------------------
    check_variants_available >> [download_dnabert2_weights, download_hyenadna_weights]
    download_dnabert2_weights >> run_dnabert2_inference
    download_hyenadna_weights >> run_hyenadna_inference
    [run_dnabert2_inference, run_hyenadna_inference] >> run_llm_ensemble_narrative
    run_llm_ensemble_narrative >> update_superset_dashboards
    update_superset_dashboards >> update_pipeline_run
